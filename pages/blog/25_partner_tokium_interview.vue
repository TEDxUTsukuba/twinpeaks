                <h2>入社のきっかけ</h2>

                <p><strong>久保</strong>: まずは、御社に入社されたきっかけから教えてください。</p>
                <p><strong>瀬戸さん</strong>: 入社を決めた一番大きな理由は、会社の理念に強く惹かれたからです。私たちは「未来へ繋がる時を生む」という考え方を大切にしていて、言い換えると、人の時間を無駄にしないための仕事をしている会社です。その理念にとても共感しました。</p>
                <p><strong>瀬戸さん</strong>: もともと私は、将来的に教員になることも考えていたのですが、その前に一度社会に出て経験を積みたいと思っていました。せっかくなら、これから成長していくフェーズにある会社で、自分自身も挑戦しながら成長したいと考えて、今の会社を選びました。</p>
                <p><strong>瀬戸さん</strong>: 加えて、実際に関わる中で感じた社員の人柄の良さも大きな理由です。チームで動く意識が強く、ホスピタリティの高い方が多く、このような人たちと一緒に働きたいと思えたことも入社の決め手でした。</p>

                <h2>現在の事業について</h2>

                <p><strong>久保</strong>: 現在はどのような事業を展開されているのでしょうか。</p>
                <p><strong>瀬戸さん</strong>: 私たちは主に経理領域において、企業の経理業務の無駄を減らし、あらゆる経理作業から解放することを目指してサービスを展開しています。どんな会社にも経理業務はありますが、その中には、手作業だったり、確認のために何度もやり取りが発生したりと、どうしても多くの時間がかかってしまう業務があります。そうした負担を減らし、お客様を煩雑な作業から解放していくことが、サービスの核になっています。</p>
                <p><strong>瀬戸さん</strong>: 最近は、単にソフトウェアを提供して業務を効率化するだけではなく、AIエージェントを活用した「デジタル労働力」の提供へと、より大きな方向に進んでいます。</p>

                <h2>デジタル労働力が目指す未来</h2>

                <p><strong>久保</strong>: 「デジタル労働力」という言葉がとても印象的です。今後はどのような未来を目指しているのでしょうか。</p>
                <p><strong>瀬戸さん</strong>: 最終的には、人が細かな経理作業を一つひとつ処理しなくてもよくなる状態を目指しています。例えば、AIが会社ごとのルールや運用、いわゆる暗黙知まで学習してくれれば、人間は最後の確認やチェックだけを行えばよい、という形に近づいていきます。</p>
                <p><strong>瀬戸さん</strong>: 経費精算や承認フローのように、これまで人が当たり前に対応してきた業務も、実は細かな判断の積み重ねで成り立っています。そこをAIが担えるようになれば、経理業務そのものから人を解放できる。そうした未来を本気で目指している会社です。</p>

                <img src="/blog/25_partner_tokium_interview/002.jpg" alt="久保と瀬戸さんのインタビュー風景" style="width: 100%; margin: 1rem 0;">

                <h2>学生団体への支援</h2>

                <p><strong>久保</strong>: 御社は学生団体への支援にも積極的に取り組まれていますが、そこにはどのような背景があるのでしょうか。</p>
                <p><strong>瀬戸さん</strong>: 大きな理由の一つは、新卒の活躍をとても重視しているからです。実際、社内でも新卒で入社して活躍しているメンバーは多く、部長や課長といったいわゆるマネージャー職の中にも新卒出身者がたくさんいます。会社にとって新卒は、とても大切な存在なんです。</p>
                <p><strong>瀬戸さん</strong>: また、単なる採用活動というよりも、もっと身近なところから学生との接点をつくりたいという思いがあります。会社の魅力は、言葉だけで説明するよりも、実際に関わる人を通して伝わる部分が大きいと思っています。だからこそ、支援を通じて会社の雰囲気や人の温かさを感じてもらうことを大事にしています。</p>

                <h2>学生団体との印象的なエピソード</h2>

                <p><strong>久保</strong>: 実際に学生団体と関わる中で、印象に残っていることはありますか。</p>
                <p><strong>瀬戸さん</strong>: はい。体育会系の団体と関わる機会があったのですが、その中で、先輩としてアドバイスしたことをすごく感謝してもらえたり、イベントが成功したときに一緒に喜べたりしたことが印象に残っています。</p>
                <p><strong>瀬戸さん</strong>: 試合観戦に行ったときに、顔見知りの学生が声をかけてくれたこともありました。そういう瞬間に、「やっていてよかったな」と感じます。やはり原点にあるのは、人の温かさだと思います。</p>
                <p><strong>瀬戸さん</strong>: また、こうした活動は採用の面でも大きな意味があります。就職活動において、学生にとって企業はどうしても少し遠い存在になりがちですが、支援を通じて接点ができることで、会社を身近に感じてもらえる、その最初の窓口になれることは、とても大きいと感じています。</p>

                <h2>就職活動を控える学生へのメッセージ</h2>

                <p><strong>久保</strong>: 就職活動を控える学生に向けて、伝えたいことがあれば教えてください。</p>
                <p><strong>瀬戸さん</strong>: 就職活動では、「何をするか」ももちろん大事ですが、それ以上に「誰と働くか」を大切にしてほしいと思っています。仕事内容だけで会社を選ぶのではなく、どんな人たちがいて、どんなカルチャーがあって、自分がその環境で前向きに働けるかまで含めて考えてみてほしいです。</p>
                <p><strong>瀬戸さん</strong>: 実際に働くうえでは、日々接する人や組織の空気感がとても重要です。だからこそ、自分が所属すべき環境はどこなのかを、丁寧に見てほしいと思います。</p>

                <img src="/blog/25_partner_tokium_interview/003.jpg" alt="久保と瀬戸さんのインタビュー風景" style="width: 100%; margin: 1rem 0;">

                <h2>学生団体との今後のパートナーシップ</h2>

                <p><strong>久保</strong>: 学生団体との関係について、今後どのように発展させていきたいと考えていますか。</p>
                <p><strong>瀬戸さん</strong>: 私たちは、一方的に支援する「スポンサー」というより、「パートナー」として対等な関係を築いていきたいと考えています。単にロゴを掲示して終わるような関係ではなく、一緒に活動しながら、お互いにとって価値のある関係をつくっていきたいです。</p>
                <p><strong>瀬戸さん</strong>: これまで接点のなかった人たちと出会えることは、企業にとっても大きな意味がありますし、そこから新しいビジネスチャンスや、優秀な学生との出会いが生まれることもあります。だからこそ、継続的に関わりながら、協力的な関係を育てていきたいと思っています。</p>

                <p><strong>久保</strong>: より良いパートナーシップのために、今後取り組んでいきたいことはありますか。</p>
                <p><strong>瀬戸さん</strong>: 交流イベントのように、直接関わる機会を増やしていきたいと考えています。やはり関係を深めるには、実際に会って話すことが大切ですし、そうした場があることで、お互いの理解も進みます。</p>
                <p><strong>瀬戸さん</strong>: また、パートナー団体が持っているネットワークを通じて、私たちのことを知っていただく機会が広がることにも期待しています。認知度の向上という意味では、例えば大学内でのネーミングライツのような大きな施策も有効だと考えています。日常の中で自然に企業名に触れることで、学生の中に安心感や親しみが生まれ、将来的に採用活動にもつながっていくと思います。</p>


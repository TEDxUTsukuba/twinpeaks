                <p>今回は、株式会社TOKIUMでインターンをされている筑波大生の大澤さんへインタビューをさせていただきました。インターンの内容に関するお話や、大澤さんご自身のお話、そして筑波大生に向けたメッセージを頂戴しました。</p>
                <p>株式会社TOKIUMは、支出管理クラウド「TOKIUM」によって業務効率化、DX※を実現するサービスを提供しています。</p>

                <p><strong>今井</strong>: 最後にインターンとして働く環境について教えてください。</p>
                <p><strong>高見さん</strong>: 自分自身の成長をこれほど後押ししてくれる環境はないと感じています。実務を通して幅広い経験を積めるのはもちろん、日々の業務に対して丁寧なフィードバックをいただけるため、迷いなくチャレンジできています。加えて、自社製品の1番のファンとして誇りを持って働く社員さんの姿は、私にとって大きな刺激になっています。最近は社内サークルのテニスやフットサルに参加しており、部署の垣根を超えて多くの社員さんとお話ししますが、社会人としての在り方についても「目標にしたい」と思える方々ばかりです。</p>

